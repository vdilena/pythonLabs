# 📌 OPERADORES ARITMÉTICOS BÁSICOS EN PYTHON

print("### Operaciones Aritméticas Básicas ###\n")

# Suma
print("Suma (2 + 5):", 2 + 5)

# Resta
print("Resta (9 - 8):", 9 - 8)

# Multiplicación
print("Multiplicación (9 * 2):", 9 * 2)

# División
print("División (18 / 2):", 18 / 2)  # Devuelve un float (aunque la división sea exacta)

# Módulo o resto de la división
print("Resto (9 % 2):", 9 % 2)  # Devuelve 1, porque 9 dividido 2 da 4 y sobra 1

# Potenciación
print("Potencia (4 ** 2):", 4**2)  # 4 elevado a la 2 = 16

print("\n### Operadores de Asignación Compuesta ###\n")

# Usamos variables para mostrar cómo se modifican con operadores compuestos

# Suma con asignación
numero = 7
numero += 2  # Equivale a: numero = numero + 2
print("Suma con asignación (7 += 2):", numero)

# Resta con asignación
numero = 8
numero -= 2  # Equivale a: numero = numero - 2
print("Resta con asignación (8 -= 2):", numero)

# Multiplicación con asignación
numero = 3
numero *= 6  # Equivale a: numero = numero * 6
print(f"Multiplicación con asignación (3 *= 6): {numero}")

# División con asignación
numero = 6
numero /= 2  # Equivale a: numero = numero / 2
print(f"División con asignación (6 /= 2): {numero}")
