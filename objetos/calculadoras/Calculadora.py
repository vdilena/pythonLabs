

# Clase con metodos estaticos
class Calculadora:

    @staticmethod
    def sumar(a,b):
        return a + b
    
    @staticmethod
    def restar(a,b):
        return a - b
    
    @staticmethod
    def multiplicar(a,b):
        return a * b
    
    @staticmethod
    def dividir(a,b):
        return a / b
    
    @staticmethod
    def potencia(a,b):
        return pow(a, b)